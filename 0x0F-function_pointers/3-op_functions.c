#include "3-calc.h"

/**
 * op_add - sum of 2 integers
 * @a: 1st parameter
 * @b: 2nd parameter
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - substraction of 2 integers
 * @a: 1st parameter
 * @b: 2nd parameter
 * Return: a - b
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiplication of 2 intgers
 * @a: 1st parameter
 * @b: 2nd parameter
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - division of 2 integers
 * @a: 1st parameter
 * @b: 2nd parameter
 * Return: division of a by b
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - modulo of a / b
 * @a: 1st parameter
 * @b: 2nd parameter
 * Return: the remainder of the division of a by b
 */
int op_mod(int a, int b)
{
return (a % b);
}
